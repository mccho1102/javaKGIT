package cal;

public class CalTest1 {

	public static void main(String[] args) {
		// 한 줄 지우기 컨트롤+d
		

		//산술 연산자
		int x = 200;
		int y = 100;
		//복사해서 붙여넣기: 컨트롤 + 알트 + 화살표
		//이동: 알트+화살표
		System.out.println("두수의 합은:" + (x + y));
		System.out.println("두수의 차은:" + (x - y));
		System.out.println("두수의 곱은:" + (x * y));
		System.out.println("두수의 나눗셈은:" + (x / y));

		
	}

}
